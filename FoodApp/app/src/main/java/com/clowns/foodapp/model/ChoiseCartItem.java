package com.clowns.foodapp.model;

public class ChoiseCartItem {
    public String name;
    public float price;

    public ChoiseCartItem(String name, float price) {
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }
}
